def compute(s: str) -> int:
    # numbers = [int(line) for line in s.splitlines()]
    # for n in numbers:
    #     pass

    lines = s.splitlines()

    unique_lens = [2, 3, 4, 7] # 1, 7, 4, 8
    total = 0
    for line in lines:
        _, outputs_raw = line.split(" | ")

        outputs = outputs_raw.split()

        for output in outputs:
            if len(output) in unique_lens:
                total += 1

    return total


